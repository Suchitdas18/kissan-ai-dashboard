        // Fallback to local knowledge base
        response = this.generateResponse(userMessage.toLowerCase());
        
        this.hideTyping();
        this.addMessage('assistant', response);
        
        // Auto-speak response
        setTimeout(() => {
            this.speak(response);
        }, 500);
